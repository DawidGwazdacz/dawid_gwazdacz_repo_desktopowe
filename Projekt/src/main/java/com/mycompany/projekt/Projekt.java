    private void jZapiszActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jZapiszActionPerformed
        String imie = jImie.getText();
        String nazwisko = jNazwisko.getText();
        String klasa = jKlasa.getSelectedItem().toString();
        String przedmiot = jPrzedmiot.getSelectedItem().toString();
        String ocena = "" + jOcena.getValue();
        try {
        FileWriter fw = new FileWriter(f);
            fw.write(imie+";");
            fw.write(nazwisko+";");
            fw.write(klasa+";");
            fw.write(przedmiot+";");
            fw.write(ocena);
            jTextArea1.setText(" ");
            fw.close();
        } catch (IOException ex) {
            Logger.getLogger(Projekt.class.getName()).log(Level.SEVERE, null, ex);
        }
      
    }//GEN-LAST:event_jZapiszActionPerformed

    private void jWczytajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jWczytajActionPerformed
        try{
            Scanner sc = new Scanner(f);
            String data = " ";
            while(sc.hasNext()){
                data += sc.nextLine()+"\n";
            }
            jTextArea1.setText(data);
            
        }   catch(FileNotFoundException ex) {
            Logger.getLogger(Projekt.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jWczytajActionPerformed

